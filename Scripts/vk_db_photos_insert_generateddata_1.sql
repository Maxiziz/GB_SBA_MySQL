UPDATE media, photos set media.media_type_id='1' where media.id=photos.media_id;

SELECT DISTINCT media.id, media.media_type_id, media_types.name from photos
LEFT JOIN media on photos.media_id = media.id
LEFT JOIN media_types on media.media_type_id = media_types.id; -- верно

